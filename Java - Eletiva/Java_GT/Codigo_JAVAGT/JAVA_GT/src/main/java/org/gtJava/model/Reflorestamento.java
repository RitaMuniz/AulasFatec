package org.gtJava.model;

public class Reflorestamento {

    private int id;
    private int ano;
    private double valorRecente;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    public double getValorRecente() {
        return valorRecente;
    }

    public void setValorRecente(double valorRecente) {
        this.valorRecente = valorRecente;
    }
}
